                <p>In today's cut-throat competition, every individual aspiring to make a mark in the corporate
                    world needs to have a good skill set in place. Not that in the earlier times, it was any
                    different but the importance has increased significantly now!</p>


                <p>Skills like multi tasking, leadership, flexibility, specialist skills, inquisitiveness,
                    ability to handle pressure and many more such as these are what the employers look for in a
                    potential employee. So, it is important that one works to develop and hone such skills. And
                    there are a number of ways in which one can do this.</p>
                <p>Internship- First being the option of going for an internship. There is an increasing trend
                    among young college students to be interning, as this gives them first hand good practical
                    experience of handling work responsibilities, dealing with people and also makes them
                    understand where their true interest lies. It also adds to one’s resume.</p>
                <p>Short term courses- There are a number of short term courses in almost every field, be it
                    photography, languages, writing, event management, designing, cooking etc. One can enroll
                    for these with a good institute and learn a new skill, especially during holidays when one
                    has ample free time. These courses add to one’s confidence and existing skill set. One may
                    choose courses which are either related to what we wish to pursue further or a completely
                    different skill.</p>
                <p>Learn skills online- You don’t just necessarily need to go for formal courses or internship,
                    but you may also search on the internet about how to learn something new. There are just
                    plenty of things given on the internet which one can learn if one has interest. Internet has
                    made everything simple, so has it made learning a new skill. Youtube gives you complete
                    videos to demonstrate how to go about doing new things. Also blogging sites, quora, pintrest
                    and such other options also give us the opportunity to expand one’s knowledge base.</p>
                <p>So, one just needs to have the willingness to learn and acquire a new skill, because there
                    are many options today that one may choose. These skills go a long way in developing the
                    personality of an individual to make him a well rounded person.</p>

